package com.fumec.androidgames.entity;

public class Game {
	
	private String id;
	private String name;
	private String thumbnail_url;
	private String android_package;
	private String web_url;
	private String url;
	private String android_price;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getThumbnail_url() {
		return thumbnail_url;
	}
	public void setThumbnail_url(String thumbnail_url) {
		this.thumbnail_url = thumbnail_url;
	}
	public String getAndroid_package() {
		return android_package;
	}
	public void setAndroid_package(String android_package) {
		this.android_package = android_package;
	}
	public String getWeb_url() {
		return web_url;
	}
	public void setWeb_url(String web_url) {
		this.web_url = web_url;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getAndroid_price() {
		return android_price;
	}
	public void setAndroid_price(String android_price) {
		this.android_price = android_price;
	}
	

}
